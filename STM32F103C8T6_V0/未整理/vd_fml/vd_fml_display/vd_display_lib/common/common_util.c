/**
 * @brief : 计算32crc校验
 *
 * @param data
 * @param len
 * @return vd_uint32_t
 */
vd_uint32_t common_crc32(vd_uint8_t* pStart, vd_uint32_t uSize) {
#define INIT  0xffffffffL
#define XOROT 0xffffffffL
    if (pStart == NULL || !uSize) {
        return 0;
    }
    vd_uint32_t uCRCValue;
    vd_uint8_t* pData;

    uCRCValue = INIT;
    pData     = pStart;

    while (uSize--) {
        uCRCValue = crc32c_table[(uCRCValue ^ *pData++) & 0xFFL] ^ (uCRCValue >> 8);
    }

    return uCRCValue ^ XOROT;
}

/**
 * @brief : 计算32crc校验
 *
 * @param data
 * @param len
 * @return vd_uint32_t
 */
vd_uint32_t common_crc32_gen(vd_uint32_t* crc, vd_uint8_t* pStart, vd_uint32_t uSize) {
#define INIT  0xffffffffL
#define XOROT 0xffffffffL
    vd_uint32_t uCRCValue;
    vd_uint8_t* pData;
    if (crc == vd_null) {
        return 0;
    }
    if (pStart == NULL || !uSize) {
        *crc = (*crc) ^ XOROT;
        return *crc;
    }

    uCRCValue = *crc;
    pData     = pStart;

    while (uSize--) {
        uCRCValue = crc32c_table[(uCRCValue ^ *pData++) & 0xFFL] ^ (uCRCValue >> 8);
    }
    *crc = uCRCValue;
    return *crc;
}

/**
 * @brief : 计算8位异或值校验
 *
 * @param data
 * @param len
 * @return vd_uint32_t
 */
vd_uint8_t common_bbc8(vd_uint8_t* pStart, vd_uint32_t uSize) {
    vd_uint8_t bcc_sum = pStart[0];
    // log_debug("start:bbc=%x\r\n", bcc_sum);
    for (int i = 1; i < uSize; i++) {
        bcc_sum ^= pStart[i];
        // log_debug("bcc_sum[%d]=%x\r\n", i, bcc_sum);
    }
    // log_debug("end:bbc=%x\r\n", bcc_sum);
    return bcc_sum;
}