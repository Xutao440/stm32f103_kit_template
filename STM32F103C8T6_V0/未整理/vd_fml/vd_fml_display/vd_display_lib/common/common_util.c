/**
 * @brief : 计算8位和校验
 *
 * @param dst
 * @param len
 * @return vd_uint8_t
 */
vd_uint8_t common_sum8(vd_uint8_t* dst, vd_uint32_t len) {
    vd_uint8_t sum = 0;
    int        i   = 0;

    for (i = 0; i < len; i++) {
        sum = (vd_uint8_t)((sum + dst[i]) & 0xFF);
    }

    return sum;
}

