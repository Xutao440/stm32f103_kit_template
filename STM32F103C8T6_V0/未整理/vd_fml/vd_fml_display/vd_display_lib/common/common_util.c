uint16_t common_crc16_gen(uint16_t last, const vd_uint8_t* data, vd_uint32_t len) {
    vd_uint8_t  da    = 0;
    vd_uint32_t i     = 0;
    uint16_t    crchl = 0;

    crchl = last;

    for (i = 0x00; i < len; i++) {
        da = (crchl / 256);
        crchl <<= 0x08;
        crchl ^= crc16_table[da ^ data[i]];
    }

    return crchl;
}

/**
 * @brief :计算16位crc校验
 *
 * @param data
 * @param len
 * @return vd_uint16_t
 */
vd_uint16_t common_crc16(vd_uint8_t* data, vd_uint32_t len) {
    vd_uint8_t  BITS_OF_BYTE  = 8;
    vd_uint16_t POLYNOMIAL    = 0xA001;
    vd_uint16_t INITIAL_VALUE = 0xFFFF;
    int         i             = 0;
    int         j             = 0;

    vd_uint16_t res = INITIAL_VALUE;
    for (i = 0; i < len; i++) {
        res = res ^ (data[i] & 0xFF);
        for (j = 0; j < BITS_OF_BYTE; j++) {
            res = (res & 0x0001) == 1 ? (res >> 1) ^ POLYNOMIAL : res >> 1;
        }
    }
    return res;
}

